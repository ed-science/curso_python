# Tipo de dado bool (booleano)
# Ao questionar algo, o resultado é sempre verdadeiro ou falso
# só existem duas respostas possíveis:
# sim(True) ou não(False)
# Existem vários operadores para "questionar" algo
print(10 == 10)  # True
print(10 == 11)  # False
print(10 != 11)  # True
print(10 != 10)  # False
print(type(False))
print(type(True))
print(10 > 11)  # False



